#include "3-calc.h"

/**
 * op_add - the sum of two integers
 * @a: 1st integer
 * @b: 2nd integer
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - the difference of two integers
 * @a: 1st integer
 * @b: 2nd integer
 * Return: difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - the product of two integers
 * @a: 1st integer
 * @b: 2nd integer
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - the product of two integers
 * @a: 1st integer
 * @b: 2nd integer
 * Return: division of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - the remainder of the division of two integers
 * @a: 1st integer
 * @b: 2nd integer
 * Return: remainder of num1 and num2
 */

int op_mod(int a, int b)
{
	return (a % b);
}
